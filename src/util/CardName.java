package util;



// Using an enum for card types allows the view and controller to easily identify them.
// This approach improves readability and organization compared to defining constants in GameConstants.
// at least I think so :)
/**
 * The enum contains the card names, which are global to all the program
 * */
public enum CardName {
    ARIADNE_CARD,
    MINOTAUR_CARD,
    NUMBER_CARD
}
